public class SignupTwoData {
    public String religion;
    public String category;
    public String income;
    public String qualification;
    public String occupation;
    public String pan;
    public String aadhar;
    public String seniorCitizen;
    public String existingAccount;

    public SignupTwoData(String religion, String category, String income, String qualification,
                         String occupation, String pan, String aadhar, String seniorCitizen,
                         String existingAccount) {
        this.religion = religion;
        this.category = category;
        this.income = income;
        this.qualification = qualification;
        this.occupation = occupation;
        this.pan = pan;
        this.aadhar = aadhar;
        this.seniorCitizen = seniorCitizen;
        this.existingAccount = existingAccount;
    }
}

